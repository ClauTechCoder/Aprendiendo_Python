# Operadores aritméticos(+,-,/,*,+=,-=,*=,/=...)

print(12 + 5)
print(12 - 5)
print(12 * 5)
print(12 / 5)
print(12 % 5) # es el de módulo (me da el resto de la operacion)
print(12 // 5) # division aproximada a un numero entero
print(3 ** 3) # calculo de un exponente

print("Hola " * 5) # con esto tengo 'hola' 5 veces seguidas only ints
print("Hola " * (2*4))


